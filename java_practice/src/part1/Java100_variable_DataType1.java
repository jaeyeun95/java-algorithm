package part1;

public class Java100_variable_DataType1 {
    public static void main(String[] args) {
        int a;
        int b;
        int c = 90;
        double d;
        char e;

        a = 10;
        b = 20;
        d = 10.0;
        e = 'A';

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

        short f = 'A';
        System.out.println(f);
    }
}
